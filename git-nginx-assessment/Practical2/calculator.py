def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def modulus(a, b):
    return a % b

print("Sum:", add(10,5))
print("Difference:", subtract(10,5))
print("Product:", multiply(10,5))
print("Quotient:", divide(10,5))
print("Remainder:", modulus(10,5))

